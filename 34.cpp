#include <iostream>
using namespace std;
//全局变量a 
int a=3;
int main() {
	//局部变量b 
	int b=2;
	//函数内可以调用全局变量a
	cout<<a<<endl;
	//函数内部也可以使用自己定义的局部变量b
	cout<<b<<endl; 
	return 0;
}
